// Logical Operators

let a = true;
let b = false;

console.log(a && b); // false
console.log(a || b); // true
console.log(!a); // false
console.log(!b); // true
console.log(a && a); // true
console.log(a || a); // true
console.log(b && b); // false
console.log(b || b); // false
console.log(!a && b); // false
console.log(!a || b); // false
console.log(a && !b); // true
